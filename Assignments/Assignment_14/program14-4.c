int CountFour(int iNo)
{
    int iDigit=0, iFrequency=0;

    
    if(iNo < 0)
    {
        iNo = -iNo;
    }

    while(iNo !=0)
    {
        iDigit = iNo % 10;
        if(iDigit == 4)
        {
            iFrequency++;
        }
        iNo = iNo / 10;
    }
    return iFrequency;

}// End of CountFour

// Time complexity: o(N)

