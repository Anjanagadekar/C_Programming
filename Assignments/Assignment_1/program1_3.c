void Display()
{
    int i=0;
    i=5;

    while(i>=1)
    {
        printf("%d\t",i);
        i--;
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    Display();

    return 0;
}

