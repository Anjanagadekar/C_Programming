#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description :   It is used to Display 5 to 1 numbers on screen
//  Input :         int
//  Output :        int
//  Author :        Anjana Dagdu Gadekar
//  Date :          16/10/2025
