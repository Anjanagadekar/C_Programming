#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Accept
//  Description :   It is used to print number of * on screen
//  Input :         int
//  Output :        String
//  Author :        Anjana Dagdu Gadekar
//  Date :          16/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

void Accept(int iNo)
{
    int iCnt = 0;

    for(iCnt=1; iCnt<=iNo; iCnt++)
    {
        printf("*\t");
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;
    
    printf("Enter number:\n");
    scanf("%d",&iValue);

    Accept(iValue);

    return 0;
}

