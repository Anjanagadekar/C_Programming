#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CircleArea
//  Description :   It is used to calculate area of circle
//  Input:          float
//  Output :        double
//  Author :        Anjana Dagdu Gadekar
//  Date :          20/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

double CircleArea(float fRadius)
{
    double dAns=0.0;
    float PI=3.14f;

    dAns = PI * fRadius * fRadius;

    return dAns;

}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0.0f;
    double dRet = 0.0;

    printf("Enter radius");
    scanf("%f", &fValue);

    dRet=CircleArea(fValue);

    printf("Area of Circle is:%lf", dRet);

    return 0;
}

