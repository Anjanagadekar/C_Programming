#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : SquareMeter
//  Description :   It is used to accept area in square feet and convert it into squaremeter
//  Input:          int
//  Output :        double
//  Author :        Anjana Dagdu Gadekar
//  Date :          20/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

double SquareMeter(int iSqFeet)
{
    double dAns=0.0;
    dAns = (double)iSqFeet * 0.0929;
    return dAns;

}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////


int main()
{
    int iValue = 0;
    double dRet = 0.0;

    printf("Enter area in square feet");
    scanf("%d", &iValue);

    dRet=SquareMeter(iValue);

    printf("Area in squaremeter:%lf ", dRet);

    return 0;
}

