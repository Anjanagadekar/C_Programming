#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : FhtoCs
//  Description :   It is used to accept temperature in fahrenheit and convert it into celsius
//  Input:          float
//  Output :        double
//  Author :        Anjana Dagdu Gadekar
//  Date :          20/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

double FhtoCs(float fTemp)
{
    double dAns=0.0;
    dAns = (fTemp -32) * (5.0 / 9.0);
    return dAns;

}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0.0f;
    double dRet = 0.0;

    printf("Enter temperature in fahrenheit");
    scanf("%f", &fValue);

    dRet=FhtoCs(fValue);

    printf("Temperature in Celsius:%lf", dRet);

    return 0;
}

