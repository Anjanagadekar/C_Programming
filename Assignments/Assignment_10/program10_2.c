double RectArea(float fWidth, float fHeight)
{
    double dAns=0.0;
    dAns = fWidth * fHeight;
    return dAns;

}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue1 = 0.0f, fValue2=0.0f;
    double dRet = 0.0;

    printf("Enter Width");
    scanf("%f", &fValue1);

    printf("Enter Height");
    scanf("%f", &fValue2);

    dRet=RectArea(fValue1, fValue2);

    printf("Area of Rectangle is:%lf", dRet);

    return 0;
}

