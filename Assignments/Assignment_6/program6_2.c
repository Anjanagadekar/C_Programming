#include<stdio.h>
#include<stdbool.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : ChkGreater
//  Description :   It is used to check given number is greater than 100 or not
//  Input:          int
//  Output :        bool
//  Author :        Anjana Dagdu Gadekar
//  Date :          18/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

bool ChkGreater(int iNo)
{
    if(iNo >=100)
    {
        return true;
    }
    else
    {
        return false;
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0;
    bool bRet=false;

    printf("Please enter Number\n");
    scanf("%d", &iValue);

    bRet=ChkGreater(iValue);

    if(bRet==true)
    {
        printf("Greater");
    }
    else
    {
        printf("Smaller");
    }

    return 0;
}

