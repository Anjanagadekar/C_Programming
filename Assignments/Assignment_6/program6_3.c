#include<stdio.h>
#include<stdbool.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : ChkEqual
//  Description :   It is used to check whether numbers equal or not
//  Input:          int
//  Output :        bool
//  Author :        Anjana Dagdu Gadekar
//  Date :          18/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////


bool ChkEqual(int iNo1, int iNo2)
{
    if(iNo1 == iNo2)
    {
        return true;
    }
    else
    {
        return false;
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue1=0, iValue2=0;
    bool bRet=false;

    printf("Please enter two Number\n");
    scanf("%d%d", &iValue1, &iValue2);

    bRet=ChkEqual(iValue1, iValue2);

    if(bRet==true)
    {
        printf("Equal");
    }
    else
    {
        printf("Not equal");
    }

    return 0;
}

