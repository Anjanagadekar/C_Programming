//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description :   It is used to print number of * on screen
//  Input :         int
//  Output :        String
//  Author :        Anjana Dagdu Gadekar
//  Date :          16/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
  
    if(iNo < 0)
    {
        iNo=-iNo;
    }

    while(iNo > 0)
    {
        printf("*\t");
        iNo--;
    }

}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;

    printf("Enter number");
    scanf("%d", &iValue);

    Display(iValue);
    return 0;
}

