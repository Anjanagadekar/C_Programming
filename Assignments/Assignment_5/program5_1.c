void CheckEvenOdd(int num)
{
    if((num % 2) == 0)
    {
        printf("%d is even number",num);
    
    }
    else
    {
        printf("%d is odd  number", num);
    }

}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int number;

    printf("enter number\n");
    scanf("%d", &number);

    CheckEvenOdd(number);

    return 0;
}

