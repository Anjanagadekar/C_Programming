void CheckLeapYear(int year)
{
    if((year % 4 == 0 ) && (year % 400 == 0)  || (year % 100 != 0))
    {
        printf("%d is a leap year\n", year);
    
    }
    else
    {
        printf("%d is not a leap year\n", year);
    }

}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int yr;

    printf("enter a year\n");
    scanf("%d", &yr);

    CheckLeapYear(yr);

    return 0;
}

