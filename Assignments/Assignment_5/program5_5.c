int FindLargest(int x, int y, int z)
{
    if(x>=y && x>=z)
    {
        return x;
    }
    else if(y>=x && y>=z)
    {
        return y;
    }
    
     else 
    {
        return z;
    
    }

}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int a,b,c,result;

    printf("enter three numbers\n");
    scanf("%d%d%d", &a,&b,&c);

    result=FindLargest(a,b,c);

    printf("Largest number is:%d",result);

    return 0;
}

