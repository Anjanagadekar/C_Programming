//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : FactDiff
//  Description :   It is used to display difference between summation all factors & non factors of number 
//  Input:          int
//  Output :        int
//  Author :        Anjana Dagdu Gadekar
//  Date :          17/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int FactDiff(int iNo)
{
    int iCnt=0;
    int iFactorsum=0, iNonFactorSum=0; 

    if(iNo<0)
    {
        iNo=-iNo;

    }

    for(iCnt=1; iCnt<iNo;iCnt++)
    {
        
        if((iNo % iCnt)== 0)
        {
            iFactorsum=iFactorsum+iCnt;
        }
        else
        {
            iNonFactorSum=iNonFactorSum+iCnt;
        }
    }
    return iNonFactorSum-iFactorsum;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0;
    int iRet=0;

    printf("enter number\n");
    scanf("%d", &iValue);

    iRet=FactDiff(iValue);

    printf("%d", iRet);

    return 0;
}

