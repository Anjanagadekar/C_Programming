//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : SumNonFact
//  Description :   It is used to display summation all non factors of number 
//  Input:          int
//  Output :        int
//  Author :        Anjana Dagdu Gadekar
//  Date :          17/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int SumNonFact(int iNo)
{
    int iCnt=0;
    int iSum=0;

    if(iNo<=0)
    {
        iNo=-iNo;

    }

    for(iCnt=1; iCnt<=iNo;iCnt++)
    {
        
        if((iNo % iCnt)!= 0)
        {
            iSum=iSum+iCnt;
        }
    }
    return iSum;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////


int main()
{
    int iValue=0;
    int iRet=0;

    printf("enter number\n");
    scanf("%d", &iValue);

    iRet=SumNonFact(iValue);

    printf("%d", iRet);

    return 0;
}

