int MultFact(int iNo)
{
    int iCnt=0;
    int iMult=1;

    if(iNo<=0)
    {
        iNo=-iNo;

    }

    for(iCnt=1; iCnt<=(iNo/2);iCnt++)
    {
        
        if((iNo % iCnt)== 0)
        {
            iMult=iMult*iCnt;
        }
    }
    return iMult;
}

