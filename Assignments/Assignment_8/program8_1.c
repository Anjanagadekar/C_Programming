//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Number
//  Description :   It is used to accept number from user & print it is small, medium or large
//  Input:          int
//  Output :        String
//  Author :        Anjana Dagdu Gadekar
//  Date :          19/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

void Number(int iNo)
{
    if(iNo<50)
    {
        printf("small");
    }
    else if(iNo>50 && iNo<100)
    {
        printf("medium");
    }
    else if(iNo>100)
    {
        printf("Large");
    }
    else
    {
        printf("invalid input");
    }

}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0;

    printf("enter number");
    scanf("%d", &iValue);

    Number(iValue);

    return 0;
}

