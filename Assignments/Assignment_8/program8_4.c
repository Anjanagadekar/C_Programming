//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Table
//  Description :   It is used to accept number from user and display its table
//  Input:          int
//  Output :        int
//  Author :        Anjana Dagdu Gadekar
//  Date :          19/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

void Table(int iNo)
{
    int iCnt=0;
    
    if(iNo<0)
    {
        iNo=-iNo;
    }
    
    for(iCnt=1; iCnt<=10; iCnt++)
    {
        
            printf("%d\t",iCnt*iNo);
        
    }

}
// Time Complexity : O(N)

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0;

    printf("enter number\n");
    scanf("%d", &iValue);

    Table(iValue);

    return 0;
}

