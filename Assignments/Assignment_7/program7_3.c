#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description :   It is used to accept number from user & print its numbers line 
//  Input:          int
//  Output :        int
//  Author :        Anjana Dagdu Gadekar
//  Date :          19/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////


void Display(int iNo)
{
    int iCnt=0;

    if(iNo < 0)
    {
        iNo=-iNo;
    }
    
    for(iCnt=-iNo; iCnt<=iNo; iCnt++)
    {
        printf("%d\t", iCnt);
        
    }

}

// Time Complexity : O(N)

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0;

    printf("enter number");
    scanf("%d", &iValue);

    Display(iValue);

    return 0;
}

