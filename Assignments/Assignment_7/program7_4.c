#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : OddDisplay
//  Description :   It is used to accept N number from user & print all odd numbers up to N
//  Input:          int
//  Output :        int
//  Author :        Anjana Dagdu Gadekar
//  Date :          19/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

void OddDisplay(int iNo)
{
    int iCnt=0;
    
    if(iNo<=0)
    {
        iNo=-iNo;
    }
    
    for(iCnt=1; iCnt<=iNo; iCnt++)
    {
        if(iCnt %2 !=0)
        {
            printf("%d\t",iCnt );
        }
    }

}

// Time Complexity : O(N)

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0;

    printf("enter number");
    scanf("%d", &iValue);

    OddDisplay(iValue);

    return 0;
}

