//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Pattern
//  Description :   It is used to print that number of $ & * on screen
//  Input:          int
//  Output :        string
//  Author :        Anjana Dagdu Gadekar
//  Date :          19/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

void Pattern(int iNo)
{
    int iCnt=0;

    if(iNo <= 0)
    {
        iNo=-iNo;
    }

    for(iCnt=1; iCnt<=iNo; iCnt++)
    {
        printf("$\t*\t");
        
    }

}
// Time Complexity : O(N)

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0;

    printf("enter number");
    scanf("%d", &iValue);

    Pattern(iValue);

    return 0;
}

