#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : MultipleDisplay
//  Description :   It is used to accept N number from user & print first 5 multiples of N
//  Input:          int
//  Output :        int
//  Author :        Anjana Dagdu Gadekar
//  Date :          19/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

void MultipleDisplay(int iNo)
{
    int iCnt=0;
    
    if(iNo<=0)
    {
        iNo=-iNo;
    }
    
    for(iCnt=1; iCnt<=5; iCnt++)
    {
        
            printf("%d\t",iCnt*iNo);
        
    }

}

// Time Complexity : O(N)

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0;

    printf("enter number\n");
    scanf("%d", &iValue);

    MultipleDisplay(iValue);

    return 0;
}

