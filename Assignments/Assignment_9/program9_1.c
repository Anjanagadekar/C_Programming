#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description :   It is used to accept number form user print that number of * & # on screen
//  Input:          int
//  Output :        string
//  Author :        Anjana Dagdu Gadekar
//  Date :          20/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    int iCnt=0;

    if(iNo < 0)
    {
        iNo=-iNo;
    }

    for(iCnt=1; iCnt<=iNo; iCnt++)
    {
        printf("*\t");
    }

    for(iCnt=1; iCnt<=iNo; iCnt++)
    {
        printf("#\t");
    }

}

// Time Complexity : O(N)

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0;

    printf("enter number");
    scanf("%d", &iValue);

    Display(iValue);

    return 0;
}

