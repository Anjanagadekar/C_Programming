//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : OddFactorial
//  Description :   It is used to find odd factorial of given number
//  Input:          int
//  Output :        int
//  Author :        Anjana Dagdu Gadekar
//  Date :          20/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int OddFactorial(int iNo)
{
    int iCnt=0;
    int iFact=1;

    if(iNo < 0)
    {
        iNo=-iNo;
    }

    for(iCnt=1; iCnt<=iNo; iCnt++)
    {
        if(iCnt%2 != 0)
        {
            iFact = iFact*iCnt;
        }
    }
    return iFact;

}

// Time Complexity : O(N)

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0, iRet=0;

    printf("enter number");
    scanf("%d", &iValue);

    iRet=OddFactorial(iValue);

    printf("OddFactorial of number is %d", iRet);

    return 0;
}

