//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
     int iValue=0;

    printf("enter number\n");
    scanf("%d", &iValue);

    DisplayFactor(iValue);

    return 0;
}

